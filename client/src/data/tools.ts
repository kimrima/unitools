export function getToolsByCategory(categoryId: string): Tool[] {
  return allTools.filter((tool) => tool.category === categoryId);
}

export function getImplementedTools(): Tool[] {
  return allTools.filter((tool) => tool.implemented);
}

export function getPopularTools(limit: number = 8): Tool[] {
  return allTools
    .filter(t => t.implemented)
    .sort((a, b) => b.usageCount - a.usageCount)
    .slice(0, limit);
}

export function getMostUsedTools(limit: number = 16): Tool[] {
  return allTools
    .filter(t => t.implemented)
    .sort((a, b) => b.usageCount - a.usageCount)
    .slice(0, limit);
}

export function getRelatedTools(toolId: string): Tool[] {
  const tool = allTools.find((t) => t.id === toolId);
  if (!tool) return [];
  
  return tool.relatedTools
    .map((id) => allTools.find((t) => t.id === id))
    .filter((t): t is Tool => t !== undefined && t.implemented);
}

export function getToolById(toolId: string): Tool | undefined {
  return allTools.find((t) => t.id === toolId);
}

export function formatUsageCount(count: number): string {
  if (count >= 1000000) return `${(count / 1000000).toFixed(1)}M`;
  if (count >= 1000) return `${(count / 1000).toFixed(1)}K`;
  return count.toString();
}
