const CATEGORIES = [
  { id: 'smileys', label: 'Smileys' },
  { id: 'gestures', label: 'Gestures' },
  { id: 'animals', label: 'Animals' },
  { id: 'food', label: 'Food' },
  { id: 'activities', label: 'Activities' },
  { id: 'objects', label: 'Objects' },
  { id: 'symbols', label: 'Symbols' },
];

export default function EmojiSearchTool() {
  const { t } = useTranslation();
  const { toast } = useToast();
  const [search, setSearch] = useState('');
  const [category, setCategory] = useState('smileys');
  const [recentlyCopied, setRecentlyCopied] = useState<string | null>(null);

  const filteredEmojis = useMemo(() => {
    const emojis = EMOJI_DATA[category as keyof typeof EMOJI_DATA] || [];
    if (!search.trim()) return emojis;
    return emojis;
  }, [category, search]);

  const copyEmoji = useCallback(async (emoji: string) => {
    await navigator.clipboard.writeText(emoji);
    setRecentlyCopied(emoji);
    toast({ description: `${emoji} ${t('Common.actions.copied', 'Copied!')}` });
    setTimeout(() => setRecentlyCopied(null), 1000);
  }, [toast, t]);

  return (
    <div className="space-y-6">
      <Card>
        <CardContent className="p-6">
          <div className="space-y-4">
            <Tabs value={category} onValueChange={setCategory}>
              <TabsList className="flex flex-wrap h-auto gap-1">
                {CATEGORIES.map(cat => (
                  <TabsTrigger key={cat.id} value={cat.id} className="text-xs">
                    {t(`Tools.emoji-search.${cat.id}`, cat.label)}
                  </TabsTrigger>
                ))}
              </TabsList>
            </Tabs>

            <div className="grid grid-cols-8 sm:grid-cols-10 md:grid-cols-12 lg:grid-cols-16 gap-1">
              {filteredEmojis.map((emoji, i) => (
                <button
                  key={i}
                  onClick={() => copyEmoji(emoji)}
                  className={`text-2xl p-2 rounded hover-elevate active-elevate-2 transition-transform ${
                    recentlyCopied === emoji ? 'bg-green-100 dark:bg-green-900/30 scale-110' : 'bg-muted'
                  }`}
                  data-testid={`emoji-${i}`}
                >
                  {emoji}
                </button>
              ))}
            </div>

            <p className="text-sm text-muted-foreground text-center">
              {t('Tools.emoji-search.clickToCopy', 'Click any emoji to copy')}
            </p>
          </div>
        </CardContent>
      </Card>
    </div>
  );
}
